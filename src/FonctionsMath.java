public class FonctionsMath {
    public static void main(String[] args) {
        double nombre=81;
        double nombre2=25;

        // Calculer la racine carré de 81
        System.out.println(Math.sqrt(nombre)); // Math.sqrt = fonction qui permet de calculer racine

        // Elever 25 à la puissance 3
        System.out.println(Math.pow(nombre2,3));

    }
}
